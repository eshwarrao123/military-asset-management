const mongoose = require('mongoose');

const logSchema = new mongoose.Schema(
    {
        actionType: {
            type: String,
            required: true,
        },
        user: {
            type: mongoose.Schema.Types.ObjectId,
            required: true,
            ref: 'User',
        },
        details: {
            type: String,
            required: true,
        },
        timestamp: {
            type: Date,
            default: Date.now,
        },
    },
    {
        timestamps: true,
    }
);

const Log = mongoose.model('Log', logSchema);
module.exports = Log;
